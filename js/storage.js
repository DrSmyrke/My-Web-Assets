/**
 * 
 * @param {boolean} session (default:true) [if true use session storage else local storage]
 * @param {string} key
 * @return dataArray or undefined if error
*/
function getStorageData( session = true, key = '' )
{
	if( app.debug ) console.log( "getStorageData >:", localStorage, sessionStorage, key );
	if( key == '' ) return undefined;
	let str = ( session ) ? sessionStorage.getItem( key ) : localStorage.getItem( key );
	if( app.debug ) console.log( "getStorageData >:", str );
	if( str == null ) str = '[]';
	return JSON.parse( str );
}

